# Crie um programa que pede 5 números
# inteiros pelo teclado e imprime as seguintes
# informações:
# – a soma de todos os números
# – o produto de todos os números

numero1 = float(input("Digite um número:"))
numero2 = float(input("Digite um número:"))
numero3 = float(input("Digite um número:"))
numero4 = float(input("Digite um número:"))
numero5 = float(input("Digite um número:"))

soma = (numero1+numero2+numero3+numero4+numero5)
produto = (numero1*numero2*numero3*numero4*numero5)

print("A soma dos 5 números é:", soma)
print("O produto dos 5 números é:", produto)
